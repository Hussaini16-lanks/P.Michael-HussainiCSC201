// Read 2 numbers and print their sum, difference and product
#include <iostream>
using namespace std;

int main() {
    int num1, num2;

    cout << "Enter first numbers: ";
    cin >> num1;

    cout << "Enter second numbers: ";
    cin >> num2;

    cout << "Sum =" << num1 + num2 << endl ;
    cout << "Difference = " << num1 - num2 << endl ;
    cout << "Product = " << num1 * num2 << endl ;

    return 0;
}
